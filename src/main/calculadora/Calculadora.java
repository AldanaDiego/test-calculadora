package calculadora;

public class Calculadora {

    public Calculadora(){};
    
    public float suma(float a, float b){
        return a + b;
    }

    public float resta(float a, float b){
        return a - b;
    }

    public float mult(float a, float b){
        return a * b;
    }

    public float div(float a, float b){
        return a / b;
    }
}
